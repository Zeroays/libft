#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int		i;
	char	*p;

	i = 0;
	p = s1;
	while (*p)
		p++;
	while (*s2 && i++ < (int)n)
		*p++ = *s2++;
	*p = '\0';
	return (s1);
}
